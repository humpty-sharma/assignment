package com.example.demo;



public class Result {
	
	private long hotelId;
	private long vacantRoom;
	private long occupiedRoom;
	
	
	public long getHotelId() {
		return hotelId;
	}
	public void setHotelId(long hotelId) {
		this.hotelId = hotelId;
	}
	public long getVacantRoom() {
		return vacantRoom;
	}
	public void setVacantRoom(long vacantRoom) {
		this.vacantRoom = vacantRoom;
	}
	public long getOccupiedRoom() {
		return occupiedRoom;
	}
	public void setOccupiedRoom(long occupiedRoom) {
		this.occupiedRoom = occupiedRoom;
	}
	
}
